import java.util.*;
class ArrayListSort
{
		public static void main(String arg[])
		{
			ArrayList<String> a=new ArrayList<>();
			a.add("Jack");
			
			a.add("Lucy");
			a.add("Brain");
			a.add("Anna");
			
			for(String i:a)
			 System.out.println(i);
			
			Collections.sort(a);
			
			System.out.println();
			
			
			for(String i:a)
			 System.out.println(i);
			
			Collections.reverse(a);
			
			System.out.println();
			
			
			for(String i:a)
			 System.out.println(i);
			
		
		
		
		
		
		
		
		
		}








}
